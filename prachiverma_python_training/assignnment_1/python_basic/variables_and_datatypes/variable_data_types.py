""" Program to create variables of different data types and print their types using the type() function. """ 

age = 23                    # int
salary = 50000.0            # float
name = "Caroline Weaver"    # str
has_membership = True       # bool

print(f"Age: {age}, Type: {type(age)}")
print(f"Salary: {salary}, Type: {type(salary)}")
print(f"Name: {name}, Type: {type(name)}")
print(f"Has Membership: {has_membership}, Type: {type(has_membership)}")
